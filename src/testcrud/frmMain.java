    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String jk = "";
        if (rbL.isSelected()) jk = "L";
        if (rbP.isSelected()) jk = "P";
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String tanggal = dateFormat.format(Tanggal.getDate());

        if(txtNIS.getText().equals("") || txtNama.getText().equals("") || tanggal.equals("") || jk.equals("") ||
                txtKelas.getText().equals("") || txtEmail.getText().equals("") || txtSmp.getText().equals("") ||
                txtAlamat.getText().equals("") || txtTelp.getText().equals("")){
            alert.showMessageDialog(this, "Harap Lengkapi Data", "Error", alert.WARNING_MESSAGE);
        } else {
            String sql = "INSERT INTO t_siswa VALUES ('"+txtNIS.getText()+"', '"+txtNama.getText()
                    +"', '"+tanggal+"', '"+jk+"', '"+txtKelas.getText()+"', '"+txtEmail.getText()
                    +"', '"+txtSmp.getText()+"', '"+txtAlamat.getText()+"', '"+txtTelp.getText()+"')";
            int status = KoneksiDB.execute(sql);
            if(status == 1){
                alert.showMessageDialog(this, "Data Berhasil Ditambahkan", "SUKSES", alert.INFORMATION_MESSAGE);
                selectData();
            } else {
                alert.showMessageDialog(this, "Data Gagal Ditambahkan", "GAGAL", alert.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

