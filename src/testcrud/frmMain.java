    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        int baris = tblData.getSelectedRow();
        if(baris != -1){
            txtNIS.setText(tblData.getValueAt(baris, 0).toString());
            txtNama.setText((String) tblData.getValueAt(baris, 1));
            
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try {
                dateFormat = date.parse(tblData.getValueAt(baris, 2).toString());
            } catch (ParseException ex) {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            Tanggal.setDate(dateFormat);
            String jk = tblData.getValueAt(baris, 3).toString();
            if(jk.equals("Laki - Laki"))
                rbL.setSelected(true);
            else 
                rbP.setSelected(true);
            txtKelas.setText(tblData.getValueAt(baris, 4).toString());
            txtEmail.setText(tblData.getValueAt(baris, 5).toString());
            txtSmp.setText(tblData.getValueAt(baris, 6).toString());
            txtAlamat.setText(tblData.getValueAt(baris, 7).toString());
            txtTelp.setText(tblData.getValueAt(baris, 8).toString());
        }
    }//GEN-LAST:event_tblDataMouseClicked

    private void selectData() {
        String kolom[] = {"NIS", "Nama Siswa", "Tanggal Lahir", "Jenis Kelamin", "Kelas", "Email", "Asal SMP", "Alamat", "No Telp"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String sql = "SELECT * FROM t_siswa";
        ResultSet rs = KoneksiDB.executeQuery(sql);
        try{
            while(rs.next()){
                String nis = rs.getString(1);
                String nama = rs.getString(2);
                String tgl = rs.getString(3);
                String jk = "";
                if(rs.getString(4).equals("L")) jk = "Laki - Laki";
                else jk = "Perempuan";
                String kelas = rs.getString(5);
                String email = rs.getString(6);
                String smp = rs.getString(7);
                String alamat = rs.getString(8);
                String telp = rs.getString(9);
                String data[] = {nis, nama, tgl, jk, kelas, email, smp, alamat, telp};
                dtm.addRow(data);
            }
        } catch (SQLException e){
            Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, e);
        }
        tblData.setModel(dtm);
    }
    
